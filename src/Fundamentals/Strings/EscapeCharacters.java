package Fundamentals.Strings;

public class EscapeCharacters {
    public static void main(String[] args) {
        System.out.println("Newline example:\nHello\nWorld!");
        System.out.println("Tab example:\tHello\tWorld!");
        System.out.println("Double quote example: \"Hello World!\"");
        System.out.println("Single quote example: \'Hello World!\'");
        System.out.println("Backslash example: \\Hello World!");
        System.out.println("Backspace example: Hello\b World!");
        System.out.println("Form feed example: Hello\fWorld!");
        System.out.println("Unicode example: \u263A");
    }
}
